const I18N_LANGS = ['en', 'tr'];
let currentLanguage = detectLanguage();

function detectLanguage(){
  try{
    const stored = localStorage.getItem('lang');
    if(I18N_LANGS.includes(stored)) return stored;
  }catch(error){}
  return navigator.language?.toLowerCase().startsWith('tr') ? 'tr' : 'en';
}

function currentLang(){ return currentLanguage; }

function t(key, params = {}){
  const template = I18N_STRINGS[currentLanguage]?.[key] ?? I18N_STRINGS.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (match, name)=>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match
  );
}

function applyTranslations(root = document){
  document.title = t('app.title');
  root.querySelectorAll('[data-i18n]').forEach((element)=>{ element.textContent = t(element.dataset.i18n); });
  const attrs = { i18nTitle: 'title', i18nAria: 'aria-label', i18nPlaceholder: 'placeholder' };
  Object.entries(attrs).forEach(([dataKey, attribute])=>{
    const selector = `[data-${dataKey.replace(/[A-Z]/g, character => '-' + character.toLowerCase())}]`;
    root.querySelectorAll(selector).forEach((element)=> element.setAttribute(attribute, t(element.dataset[dataKey])));
  });
}

function setLang(nextLanguage){
  if(!I18N_LANGS.includes(nextLanguage) || nextLanguage === currentLanguage) return;
  currentLanguage = nextLanguage;
  try{ localStorage.setItem('lang', nextLanguage); }catch(error){}
  document.documentElement.lang = nextLanguage;
  applyTranslations();
  window.dispatchEvent(new CustomEvent('langchange', { detail: { lang: nextLanguage } }));
}

function initI18n(container){
  document.documentElement.lang = currentLanguage;
  const sync = ()=>{
    container?.querySelectorAll('[data-lang]').forEach((button)=>{
      const active = button.dataset.lang === currentLanguage;
      button.classList.toggle('is-active', active);
      button.setAttribute('aria-pressed', String(active));
    });
  };
  container?.querySelectorAll('[data-lang]').forEach((button)=> button.addEventListener('click', ()=> setLang(button.dataset.lang)));
  window.addEventListener('langchange', sync);
  applyTranslations();
  sync();
}
